<template>
  <div id="app">
    @{{ user.username }} - {{ fullName }}
      <p>Followers {{ followers }}</p>
      <button
        @click="followUser"
      >
        Follow
      </button>
  </div>
</template>

<script>
// import HelloWorld from './components/HelloWorld.vue'

export default {
  name: 'App',
  // components: {
  //   HelloWorld
  // }
  data() {
    return{
      followers: 0,
      user: {
        id: 1,
        username:'codestance',
        firstName: 'Connie',
        lastName: 'Stance',
        email: 'email@email.com',
        isAdmin: true
      }
    }
  },
  watch: {
    followers(newFollowerCount, oldFollowerCount){
      if(oldFollowerCount < newFollowerCount){
        console.log(`${this.user.username} has gained a folloer`)
      }
    }
  },
  computed: {
    fullName() {
      return `${this.user.firstName} ${this.user.lastName}`
    }
  },
  methods: {
    followUser() {
      this.followers++
    }
  },
  mounted() {
    this.followUser()
  }
}
</script>

